; =============================================================================
;	WORKAREA
; =============================================================================

; -----------------------------------------------------------------------------
;	H.TIMIのサイズ
; -----------------------------------------------------------------------------
h_timi_backup_size			:= 5		; 5byte(s)

; -----------------------------------------------------------------------------
;	B-SUP自身が存在する page1 の SLOT#
; -----------------------------------------------------------------------------
work_p1_slot				:=	0
size_p1_slot				:=	1		; byte(s)

; -----------------------------------------------------------------------------
;	page2 の RAM の SLOT#
; -----------------------------------------------------------------------------
work_p2_slot				:=	work_p1_slot + size_p1_slot
size_p2_slot				:=	1		; byte(s)

; -----------------------------------------------------------------------------
;	page2 の SLOT# の保存ワーク (H.TIMI用)
; -----------------------------------------------------------------------------
work_current_p2_slot		:=	work_p2_slot + size_p2_slot
size_current_p2_slot		:=	1		; byte(s)

; -----------------------------------------------------------------------------
;	page2 の SLOT# の保存ワーク (CALL命令用)
; -----------------------------------------------------------------------------
work_save_p2_slot			:=	work_current_p2_slot + size_current_p2_slot
size_save_p2_slot			:=	1		; byte(s)

; -----------------------------------------------------------------------------
;	Sprite# に対応する Sprite information#
; -----------------------------------------------------------------------------
work_1st_sprite				:= work_save_p2_slot + size_save_p2_slot
size_1st_sprite				:= 1		; byte(s)

; -----------------------------------------------------------------------------
;	Sprite Driver ON/OFF
; -----------------------------------------------------------------------------
work_sprite_drive_on		:= work_1st_sprite + size_1st_sprite
size_sprite_drive_on		:= 1		; byte(s)

; -----------------------------------------------------------------------------
;	Sprite 自動移動の一時停止
; -----------------------------------------------------------------------------
work_sprite_stop_auto_move	:= work_sprite_drive_on + size_sprite_drive_on
size_sprite_stop_auto_move	:= 1	; byte(s)

; -----------------------------------------------------------------------------
;	Sprite information area
; -----------------------------------------------------------------------------
work_sprite_info			:= work_sprite_stop_auto_move + size_sprite_stop_auto_move
size_sprite_info			:= 16 * 32	; byte(s)

; -----------------------------------------------------------------------------
;	End of WORKAREA
; -----------------------------------------------------------------------------
work_area_size				:= work_sprite_info + size_sprite_info
